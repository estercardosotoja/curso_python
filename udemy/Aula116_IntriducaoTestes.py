"""
Boas práticas de programação:
a) Deixar o código facil de entender, aplicar a indentação, organizar bem seu código.
b) Sempre refatorar o código a fim de deixá-lo o menor possível, mas sem perder o facil entendimento.
c) Atingir o objetivo do código sem dar muitas voltas.
b) Testar o código.

O que são Testes?
 - Testes são uma maneira de automatizar os testes de cada parte do seu código.
Por que devo usá-los?
 - Reduz bugs, mesmo adicionando novos codigos posteriormente.
 - Seu código adquire qualidade.
 - Cria maior acessibilidade ao seu código para ser usado por outros programadores.
Como aplicá-los?
 - Assertions,Doctests, Unittest, dentre outros.
Quando aplicá-los?
 - Sempre que possível.
Onde aplicá-los?
 - Funções, Classes são muito comuns de serem testados. Mas podem haver outras partes do código.
 - Desenvolvimento Web, Jogos, aplicativos, dentre outros.

-> TDD - Test Driven Development ou Desenvolvimento Orientado a Testes.
 - Basicamente é um passo a passo a como desenvolver programas com testes.
É dividido em 3 etapas:
a) Red: Aplique o teste no código(sem ser implementado) que irá falhar.
b) Green: Desenvolve o código para passar no teste.
c) Refactor: Refatore o código para deixá-lo mais funcional.
"""
