"""

Dir e Help

Dir: Apresenta a lista de funções e meotdos que podem ser usadas em alguns tipo de dado.

Sintaxe: dir(valor/tipo de dado)

"""

#Exemplo
print(dir('urso'))
print(dir(18))
print(dir(float))

"""

Help: Apresenta as intruções de utilização das funções e metodos disponiveis para determinado tipo de dado.

Sintaxe: help(funcao) ou help (valor/tipo de dado.metodo/funcao

"""
#Exemplo

print(help(print))
print(help('dudu'.title))

